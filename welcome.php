<h3 align="center">SISTEM PENDUKUNG KEPUTUSAN PENERIMAAN BEASISWA</h3>
<h3 align="center">Indonesia Mandiri</h3>
<br>
<center>Sistem Pendukung Keputusan (SPK) adalah sistem komputer atau aplikasi perangkat lunak yang dirancang 
    untuk membantu pengambilan keputusan dalam situasi yang kompleks atau ambigu. SPK menggunakan berbagai teknik, 
    model matematis, algoritma, dan basis data untuk menganalisis informasi yang tersedia dan menyediakan rekomendasi
    atau pemahaman yang lebih baik kepada pengambil keputusan.</center>
<br>
    Analisis Multikriteria: Metode ini memungkinkan pengambil keputusan untuk mengevaluasi beberapa kriteria atau faktor yang relevan dalam pengambilan keputusan. 
    Contohnya termasuk Metode Weighted Sum Model, Analytic Hierarchy Process (AHP), dan Technique for Order Preference by Similarity to Ideal Solution (TOPSIS).